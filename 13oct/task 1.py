## the difference between = operator and == operator in python
#  "=" is an assignment operator whereas "==" is an Relational operator
n1 = int(input("Enter a number"))
n2 = int(input("Enter a number"))
print (n1 == n2)
# When n1 and n2 has same number, True will be return as output else False


## What does ** operator do in python and how it is used
# ** is an Arithmetic operator in python often used as an exponentiation
n3 = int (input("Enter a number"))
n4 = int (input("Enter a number"))
print (n3**n4)

## What does the ^ operator do in Python, and in what context is it commonly used?
# "^" is a XOR Operator
# If only one 1 is there it sets each bit to one or if both are 0 and 1 it sets to 0
n5 = int (input("enter a number"))
n6 = int( input("enter a number"))
print (n5^n6)

